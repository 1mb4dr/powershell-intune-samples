foreach($ManagedAppPolicy in $ManagedAppPolicies){

write-host "Managed App Policy:"$ManagedAppPolicy.displayName -f Yellow

$ManagedAppPolicy

    if($ManagedAppPolicy.targetedSecurityGroupsCount -eq 1){

    write-host "targetedSecurityGroupIds:" -f Cyan

    (Get-AADGroup -id $ManagedAppPolicy.targetedSecurityGroupIds).displayName

    }

    if($ManagedAppPolicy.targetedSecurityGroupsCount -gt 1){

    write-host "targetedSecurityGroupIds:" -f Cyan

        foreach($Group in $ManagedAppPolicy.targetedSecurityGroupIds){

        (Get-AADGroup -id $Group).displayName

        }

        Write-Host
    }


    if($ManagedAppPolicy.deployedAppCount -ge 1){

    write-host "Managed App Policy - Mobile Apps" -f Cyan

    Get-ManagedAppPolicyMobileApps -id $ManagedAppPolicy.id | select mobileAppIdentifierDeployments

    }

}
